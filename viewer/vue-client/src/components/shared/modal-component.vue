<script>
/* 

  HOW TO USE:
  This component can recieve content to inject in the different slots.

  Props:
    * title         - Takes a string or an array of strings. Will try to translate into user language.
    * width         - Width value

  The slots are:
    * modal-header  - If no content, will just show the "title"-prop and a close-button, explained below.
    * modal-body    - Just a container for your content. Supports highly customized layout.

  Close-event:
    The default close button (and click on backdrop) will emit an event called "close".
    Listen to it in the parent with @close="callSomeFunction"

    Example:
      <modal-component title="My nice modal" v-if="modalActive" @close="modalActive=false"></modal-component>

*/

import { isArray } from 'lodash-es';
import { mapGetters } from 'vuex';
import * as LayoutUtil from '@/utils/layout';
import * as StringUtil from '@/utils/string';

export default {
  name: 'modal-component',
  props: {
    title: {
      default: 'Untitled modal',
      type: [String, Array],
    },
    modalType: {
      default: 'normal',
      type: String,
    },
    closeable: {
      default: true,
      type: Boolean,
    },
    width: {
      default: '',
      type: String,
    }
  },
  data() {
    return {
      fadedIn: false,
      fadeTime: 500,
    };
  },
  methods: {
    close() {
      if (this.closeable) {
        this.fadedIn = false;
        setTimeout(() => {
          this.$emit('close');
        }, this.fadeTime);
      }
    },
  },
  computed: {
    ...mapGetters([
      'user',
    ]),
    translatedTitle() {
      let title = '';
      if (isArray(this.title)) {
        for (let i = 0; i < this.title.length; i++) {
          title = `${title}${StringUtil.getUiPhraseByLang(this.title[i], this.user.settings.language)} `;
        }
      } else {
        title = StringUtil.getUiPhraseByLang(this.title, this.user.settings.language);
      }
      return title;
    },
  },
  components: {
  },
  mounted() {
    this.$nextTick(() => {
      LayoutUtil.scrollLock(true);
      setTimeout(() => {
        this.fadedIn = true;
      }, 1);
    });
  },
  beforeDestroy() {
    this.$nextTick(() => {
      LayoutUtil.scrollLock(false);
    });
  },
};
</script>

<template>
  <div class="ModalComponent" :class="{'is-fadedIn': fadedIn, 'is-danger': modalType === 'danger'}">
    <div class="ModalComponent-backdrop" @click="close"></div>
    <div class="ModalComponent-container" :style="{ 'width': width }">
      <div class="ModalComponent-header">
        <slot name="modal-header">
          <header>
            {{ translatedTitle }}
          </header>
          <span class="ModalComponent-windowControl" v-if="closeable">
            <i @click="close" class="fa fa-close icon--md"></i>
          </span>
        </slot>
      </div>
      <div class="ModalComponent-body">
        <slot name="modal-body">
          <code>No content recieved from parent</code>
        </slot>
      </div>
    </div>
  </div>
</template>

